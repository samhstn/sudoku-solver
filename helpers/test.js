tape('generateLn generates lines', t => {
  let actual;
  let expected;

  actual = generateLn();
  expected = [1, 2, 3, 4, 5, 6, 7, 8, 9];
  t.deepEqual(actual, expected, 'generates a line from 1 to 9');

  actual = generateLn(0);
  expected = [0, 0, 0, 0, 0, 0, 0, 0, 0];
  t.deepEqual(actual, expected, 'generates a line of 0s');

  actual = generateLn(1);
  expected = [1, 1, 1, 1, 1, 1, 1, 1, 1];
  t.deepEqual(actual, expected, 'generates a line of 1s');

  t.end();
});

